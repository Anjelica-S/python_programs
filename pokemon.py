import random
# global variables
pokemon_level = 0
pokemon_name = "Munchlax"
day = 1

# functions
def pokemon_game():
    global pokemon_level
    global day
    draw_munchlax()

    print("welcome to pokemon trainer! Choose an activity for day " + str(day) + "!")
    user_choice = int(input("""Train (1)
Gym Battle (2)
Rest(3)
quit(4)
"""))

    while user_choice != 4:
        if user_choice == 1:
            print("you chose to train!")
            print("your " + pokemon_name + "did " + str(random.randint(1,50)) + " pushups!")
            print("your pokemon leveled up!")
            pokemon_level = int(pokemon_level) + 1
            print(pokemon_name + " is now level " + str(pokemon_level))
        if user_choice == 2:
            print("you chose to do a gym battle!")
            win = random.randint(1,2)
            if win == 1:
                print("your " + pokemon_name + " won!")
                pokemon_level = pokemon_level + 2
                print(pokemon_name + " is now level " + str(pokemon_level))
            elif win == 2:
                print("your pokemon lost...")
        if user_choice == 3:
            print("you had a restfull day..")
            print(pokemon_name + " is now level " + str(pokemon_level))
            print("it is day " + str(day))
        day = day+1
        print("welcome back to pokemon trainer! Choose an activity for day " + str(day) + "!")
        user_choice = int(input("""1: Train
2: Gym Battle
3: Rest
4. Quit
"""))


    if pokemon_level ==5:
        print("your pokemon is evolving...")
        print("your munchlax has evlolved into a snorlax!")
        print(pokemon_name + " is now level " + str(pokemon_level))
        draw_snorlax()
    if pokemon_level ==10:
        print("your pokemon is evolving...")
        print("your snorlax has evlolved into a gigantamax_snorlax!")
        print(pokemon_name + " is now level " + str(pokemon_level))
        draw_gigantamax_snorlax()


    if user_choice == 4:
        print("thank you for playing!")

